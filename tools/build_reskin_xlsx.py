def sheet(name, headers, rows, widths, bold_cols=()):
    ws = wb.create_sheet(name)
    ws.append(headers)
    for c in ws[1]:
        c.fill = HEAD_FILL
        c.font = HEAD_FONT
        c.alignment = HWRAP
        c.border = BORDER
    for r in rows:
        ws.append(list(r))
    for ci, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(ci)].width = w
    for ri, row in enumerate(ws.iter_rows(min_row=2), start=2):
        for ci, c in enumerate(row, start=1):
            c.font = BOLD if ci in bold_cols else BODY
            c.alignment = WRAP
            c.border = BORDER
            if ri % 2 == 0:
                c.fill = ACCENT_FILL
    ws.row_dimensions[1].height = 26
    ws.freeze_panes = "A2"
    ws.sheet_view.showGridLines = False
    return ws


