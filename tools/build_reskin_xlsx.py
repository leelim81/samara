def slug(n):
    return re.sub(r"[^a-z0-9]+", "_", n.lower()).strip("_")


chapters = {c["num"]: (c["name"], slug(c["name"])) for c in data["chapters"]}


def orig_narr(s):
    p = os.path.join(ROOT, "text", "autogenerated", f"script_{s}.json")
    if not os.path.exists(p):
        return ""
    return "\n\n".join(x.get("line", "") for x in json.load(open(p)))


def orig_banter(s):
    p = os.path.join(ROOT, "text", "autogenerated", f"dialogue_{s}.json")
    if not os.path.exists(p):
        return []
    return [(x.get("speaker", ""), x.get("line", "")) for x in json.load(open(p))]


